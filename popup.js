function setAlarm(event) {
  let minutes = parseFloat(event.target.value);
  chrome.browserAction.setBadgeText({text: 'ON'});
  chrome.alarms.create({delayInMinutes: minutes});
  chrome.storage.sync.set({minutes: minutes});
  window.close();
}

function clearAlarm() {
  chrome.browserAction.setBadgeText({text: ''});
  chrome.alarms.clearAll();
  window.close();
}

//An Alarm delay of less than the minimum 1 minute will fire
// in approximately 1 minute incriments if released
document.getElementById('3-sec').addEventListener('click', setAlarm);
document.getElementById('5-sec').addEventListener('click', setAlarm);
document.getElementById('10-sec').addEventListener('click', setAlarm);
document.getElementById('off').addEventListener('click', clearAlarm);